/** Resolve a file icon the same way lazygit does: basename, extension, directory, fallback file. */
export function iconForFile(filePath: string, isDirectory = false): NerdFontIcon {
  const name = basename(filePath);
  return ICON_BY_NAME[name] ?? ICON_BY_EXT[extname(name).toLowerCase()] ?? (isDirectory ? DEFAULT_DIRECTORY_ICON : DEFAULT_FILE_ICON);
}
